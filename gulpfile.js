const browserSync = require('browser-sync').create('browserSync');
const _merge = require('lodash.merge');
const { series, parallel, watch } = require('gulp');
const path = require('path');

const defaultConfig = {
    src: {
        dir: 'src/',
        fonts: 'fonts/**/*.{woff,woff2,ttf,otf,svg}',
        images: 'img/**/*.{png,jpg,jpeg,gif,svg,ico,json,xml}',
        js: 'js/**/*.js',
        sass: 'scss/**/*.scss',
        views: ''
    },
    dist: {
        dir: 'dist/',
        fonts: 'fonts',
        images: 'img',
        js: 'js',
        css: 'css'
    },
    options: {
        autoprefixer: {},
        browsersync: {
            open: false,
            notify: false,
            proxy: ''
        },
        csscomb: path.resolve(__dirname, '.csscomb.json'),
        cssnano: {
            preset: 'default'
        },
        rucksack: {
            responsiveType: true,
            shorthandPosition: false,
            quantityQueries: false,
            inputPseudo: false,
            clearFix: false,
            fontPath: false,
            hexRGBA: false,
            easings: false,
            fallbacks: false,
            autoprefixer: false,
            reporter: false
        },
        sass: {
            includePaths: ['node_modules']
        },
        webpack: {
            // Generated by scripts task
        }
    }
};

function prettyLog(json) {
    console.log(JSON.stringify(json, null, 4));
}

module.exports = projectConfig => {
    const config = _merge({}, defaultConfig, projectConfig);

    // Build list of resolved paths to pass to tasks
    config.paths = {
        src: {},
        dist: {}
    };

    ['src', 'dist'].forEach(group => {
        Object.keys(config[group]).forEach(dirName => {
            if(dirName == 'dir' || dirName == 'views') {
                return;
            }
            config.paths[group][dirName] = path.resolve(config[group].dir, config[group][dirName])
        });
    });

    prettyLog(config);

    const cleanTask = require('./tasks/clean.js')(config);
    const cssCombTask = require('./tasks/csscomb.js')(config);
    const fontsTask = require('./tasks/fonts.js')(config);
    const imageTasks = require('./tasks/images.js')(config);
    const sassTasks = require('./tasks/sass.js')(config);
    const webpackTasks = require('./tasks/webpack.js')(config);

    const reloadBrowserSync = cb => {
        browserSync.reload();
        cb();
    };

    const watchTask = cb => {
        watch(config.paths.src.fonts, fontsTask);
        watch(config.paths.src.images, imageTasks.dev);
        watch(config.paths.src.js, series(webpackTasks.dev, reloadBrowserSync));
        watch(config.paths.src.sass, series(cssCombTask, sassTasks.dev));
        watch(config.src.views, reloadBrowserSync);
    }

    const serveTask = cb => {
        browserSync.init(config.options.browsersync);
    }

    const devBuildTask = series(
        cleanTask,
        parallel(
            fontsTask,
            imageTasks.dev,
            series(
                cssCombTask,
                sassTasks.dev
            ),
            webpackTasks.dev
        )
    );

    const prodBuildTask = series(
        cleanTask,
        parallel(
            fontsTask,
            imageTasks.prod,
            series(
                cssCombTask,
                sassTasks.prod
            ),
            webpackTasks.prod
        )
    );

    return {
        clean: cleanTask,
        csscomb: cssCombTask,
        fonts: fontsTask,
        images: imageTasks.dev,
        sass: sassTasks.dev,
        webpack: webpackTasks.dev,
        watch: watchTask,
        serve: serveTask,
        dev: devBuildTask,
        build: prodBuildTask,
        default: parallel(devBuildTask, serveTask, watchTask),
    }
};
